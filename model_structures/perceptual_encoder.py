import torch
import torch.nn as nn
import numpy as np
import math
from timm.models.vision_transformer import PatchEmbed, Attention, Mlp

def Normalize(hidden_size, elementwise_affine=False, eps=1e-6):
    return nn.LayerNorm(hidden_size, elementwise_affine=elementwise_affine, eps=eps)

def modulate(x, shift, scale):
    return x * (1 + scale.unsqueeze(1)) + shift.unsqueeze(1)

def nonlinearity(x):
    # swish
    return x*torch.sigmoid(x)

class ProjBlock(nn.Module):
    def __init__(self, hidden_size, out_channels):
        super().__init__()
        self.norm_final = Normalize(hidden_size)
        self.linear = nn.Linear(hidden_size, out_channels, bias=True)
        self.adaLN_modulation = nn.Sequential(
            nn.SiLU(),
            nn.Linear(hidden_size, 2 * hidden_size, bias=True)
        )

    def forward(self, x, c):
        shift, scale = self.adaLN_modulation(c).chunk(2, dim=1)
        x = modulate(self.norm_final(x), shift, scale)
        x = self.linear(x)
        return x

class ViTBlock(nn.Module):
    def __init__(self, hidden_size, num_heads, mlp_ratio=4.0, **block_kwargs):
        super().__init__()
        self.norm1 = Normalize(hidden_size)
        self.attn = Attention(hidden_size, num_heads=num_heads, qkv_bias=True, **block_kwargs)
        self.norm2 = Normalize(hidden_size)
        mlp_hidden_dim = int(hidden_size * mlp_ratio)
        approx_gelu = lambda: nn.GELU(approximate="tanh")
        self.mlp = Mlp(in_features=hidden_size, hidden_features=mlp_hidden_dim, act_layer=approx_gelu, drop=0)
        self.adaLN_modulation = nn.Sequential(
            nn.SiLU(),
            nn.Linear(hidden_size, 6 * hidden_size, bias=True)
        )

    def forward(self, x, c):
        shift_msa, scale_msa, gate_msa, shift_mlp, scale_mlp, gate_mlp = self.adaLN_modulation(c).chunk(6, dim=1)
        x = x + gate_msa.unsqueeze(1) * self.attn(modulate(self.norm1(x), shift_msa, scale_msa))
        x = x + gate_mlp.unsqueeze(1) * self.mlp(modulate(self.norm2(x), shift_mlp, scale_mlp))
        return x

    

class ResBlock(nn.Module):
    def __init__(self, in_channels, out_channels, condition=False, residual=True):
        super().__init__()
        self.residual = False if in_channels != out_channels else residual
        out_channels = in_channels if out_channels is None else out_channels

        self.norm1 = Normalize(in_channels)
        self.linear1 = nn.Linear(in_channels, out_channels, bias=True)
        self.norm2 = Normalize(out_channels)
        self.linear2 = nn.Linear(out_channels, out_channels, bias=True)

        if condition:
            self.norm_c1 = Normalize(out_channels)
            self.adaLN_modulation1 = nn.Sequential(
                nn.SiLU(),
                nn.Linear(out_channels, 2 * out_channels, bias=True)
            )
            self.norm_c2 = Normalize(out_channels)
            self.adaLN_modulation2 = nn.Sequential(
                nn.SiLU(),
                nn.Linear(out_channels, 2 * out_channels, bias=True)
            )

    def forward(self, x, c=None):
        h = x
        if c is not None:
            shift, scale = self.adaLN_modulation1(c).chunk(2, dim=1)
            h = modulate(self.norm_c1(h), shift, scale)
        h = self.norm1(x)
        h = nonlinearity(h)
        h = self.linear1(h)

        if c is not None:
            shift, scale = self.adaLN_modulation2(c).chunk(2, dim=1)
            h = modulate(self.norm_c2(h), shift, scale)
        h = self.norm2(h)
        h = nonlinearity(h)
        h = self.linear2(h)

        if self.residual:
            x = x+h
        else:
            x = h
        return x

class ResNet1D(nn.Module):
    def __init__(
        self,
        input_size=32,
        in_channels=4,
        hidden_size=4096,
        out_channels = None,
        depth=12,
        condition = False
    ):
        super().__init__()
        self.in_channels = input_size ** 2 * in_channels
        self.hidden_size = hidden_size
        self.out_channels = hidden_size if out_channels is None else out_channels
        
        if condition:
            self.t_embedder = TimestepEmbedder(hidden_size)

        self.blocks = nn.ModuleList(
            [ResBlock(in_channels=self.in_channels, 
                     out_channels=self.out_channels, 
                     condition=condition, 
                     residual=True)]+
            [ResBlock(in_channels=self.hidden_size, 
                     out_channels=self.hidden_size, 
                     condition=condition, 
                     residual=True) for _ in range(depth)
            ]+
            [ResBlock(in_channels=self.hidden_size, 
                     out_channels=self.out_channels, 
                     condition=condition, 
                     residual=True)])
        
        self.initialize_weights()

    def initialize_weights(self):
        # Initialize transformer layers:
        def _basic_init(module):
            if isinstance(module, nn.Linear):
                torch.nn.init.xavier_uniform_(module.weight)
                if module.bias is not None:
                    nn.init.constant_(module.bias, 0)
        self.apply(_basic_init)

    def forward(self, x, t=None):
        
        x = x.view(x.shape[0], -1)  
        #print(f'x: {x.shape}')
        if t is not None:
            t = self.t_embedder(t)               
            #print(f't: {t.shape}')                            
        for block in self.blocks:
            x = block(x, t)   
            #print(f'x: {x.shape}')                   
        return x
    

class ResNet1D_Classifier(nn.Module):
    def __init__(
        self,
        input_size=32,
        in_channels=4,
        hidden_size=4096,
        out_channels = 4096,
        depth=12,
        class_num = 1000,
        condition = False
    ):
        super().__init__()
        self.in_channels = input_size ** 2 * in_channels
        self.hidden_size = hidden_size
        self.out_channels = out_channels
        self.class_num = class_num

        self.resnet = ResNet1D(input_size=input_size,
                               in_channels=in_channels,
                               hidden_size=hidden_size,
                               out_channels=out_channels,
                               depth=depth,
                               condition=condition)
        self.classifier = nn.Linear(self.out_channels, self.class_num)
        self.sigmoid = nn.Sigmoid()
        self.initialize_weights()

    def initialize_weights(self):
        # Initialize transformer layers:
        def _basic_init(module):
            if isinstance(module, nn.Linear):
                torch.nn.init.xavier_uniform_(module.weight)
                if module.bias is not None:
                    nn.init.constant_(module.bias, 0)
        self.apply(_basic_init)

    def forward(self, x):
        x = self.resnet(x)
        x = self.classifier(x)
        return x

